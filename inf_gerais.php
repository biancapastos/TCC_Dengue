	<ul>

		<li> 
			<h5 class = "inf_g">O que é Dengue?</h5>
			<p class = "justificado">
				A Dengue é uma doença viral transmitida por mosquitos que ocorre normalmente em áreas tropicais e subtropicais.
			</p>
		</li>
		
		<li> 
			<h5 class = "inf_g">Sintomas</h5>
			<p class = "justificado">
				A infecção pode ocorrer sem manifestar sintoma algum! <br />
				Porém, normalmente se apresenta primeiro febre alta (39° a 40°C) que costuma durar de 2 a 7 dias,
				acompanhada de dor de cabeça, dores no corpo e nas articulações, além de prostração, fraqueza, dor atrás dos olhos, erupção e coceira na pele.
				Outros sintomas comuns são a perda de peso, náuseas, vômitos, manchas vermelhas no corpo e dores musculares intensas.
				Na forma mais grave da doença ocorrem dores abdominais intensas e contínuas, além de vômitos persistentes e sangramento de mucosas.
				É preciso ficar atento com a possibilidade de queda abrupta das plaquetas.
				</br><b>É importante procurar serviço de saúde para o diagnóstico e o tratamento da doença.</B>
			</p>
		</li>
		
		<li>
			<h5 class = "inf_g">Formas de Transmissão</h5> 
			<p class = "justificado">
				A dengue é transmitida pela picada do mosquito Aedes Aegypti.<br />
				A transmissão do vírus ocorre a partir da picada da fêmea infectada com um dos quatro sorotipos do vírus.
				A doença não é transmissível através do contato, porém o virus pode ser passado por meio de transfusão sanguínea. 
			</p>
		</li>
		
		<li> 
			<h5 class = "inf_g">Como se Prevenir</h5> 
			<p class = "justificado">
			A melhor forma de se prevenir contra a dengue é evitar a proliferação do mosquito Aedes Aegypti.
			Para isso, é preciso eliminar possíveis criadouros do mosquito, que se formam em recipientes com água parada,
			como em vasos de plantas, lagões de água, pneus, garrafas plásticas, piscinas sem uso e sem manutenção,
			e até mesmo em recipientes pequenos, como tampas de garrafas ou potes de comida e água para animais de estimação. <br />
			Existem alguns truques para se proteger da picada, como utilizar roupas que minimizem a exposição da pele durante o dia,
			quando os mosquitos são mais ativos, utilizar repelentes e inseticidas. 
			Mosquiteiros proporcionam boa proteção para aqueles que dormem durante o dia, como bebês, pessoas acamadas e trabalhadores noturnos.
			</p>
		</li>
		
		<li> 
			<h5 class = "inf_g">Tratamento</h5>
			<p class = "justificado">
			Não existe tratamento específico para a dengue.<br />
			<b>Em caso de suspeita é fundamental procurar um profissional de saúde para o correto diagnóstico. </b> <br />
			A assistência em saúde é feita para aliviar os sintomas.
			Estão entre as formas de tratamento: fazer repouso, ingerir bastante líquido (água) e não tomar medicamentos por conta própria.<br />
			O tratamento deve ser feito sempre de acordo com avaliação de um profissional de saúde, conforme cada caso.
			</p>
		</li>
		
		<li> 
			<h5 class = "inf_g">Diagnóstico</h5>
			<p class = "justificado">
			O diagnóstico da dengue é clínico e feito por um médico. 
			É confirmado com exames laboratoriais de sorologia, de biologia molecular e de isolamento viral,
			ou confirmado com teste rápido (usado para triagem). 
			Todos os exames estão disponíveis no Sistema Único de Saúde (SUS).
			</p>
		</li>

